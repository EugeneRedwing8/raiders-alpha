function generateWorld () {
    if (!(blockSettings.exists("Tilemap"))) {
        Tileimage1 = img`
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            ........................................
            `
        x = 0
        y = 0
        for (let index = 0; index < 30; index++) {
            for (let index = 0; index < 40; index++) {
                PlainsTileList = [
                6,
                6,
                6,
                11,
                11,
                2
                ]
                if (Math.percentChance(95)) {
                    Tileimage1.setPixel(x, y, 7)
                } else {
                    Tileimage1.setPixel(x, y, PlainsTileList[randint(0, PlainsTileList.length - 1)])
                }
                x += 1
            }
            x = 0
            y += 1
        }
        x = 0
        y = 0
        for (let index = 0; index < 30; index++) {
            for (let index = 0; index < 40; index++) {
                if (Math.percentChance(1)) {
                    CliffWidth = randint(3, 12)
                    CliffHeight = randint(1, 8)
                    tx = x
                    ty = y
                    for (let index = 0; index < CliffWidth; index++) {
                        Tileimage1.setPixel(x, y, 4)
                        x += 1
                    }
                    y += 1
                    x = tx
                    for (let index = 0; index < CliffHeight - 1; index++) {
                        for (let index = 0; index < CliffWidth; index++) {
                            Tileimage1.setPixel(x, y, 14)
                            x += 1
                        }
                        y += 1
                        x = tx
                    }
                    x = tx
                    y = ty
                }
                x += 1
            }
            x = 0
            y += 1
        }
        blockSettings.writeImage("Tilemap", Tileimage1)
        scene.setTileMap(Tileimage1, TileScale.Eight)
        InterpretTiles()
    } else {
        scene.setTileMap(blockSettings.readImage("Tilemap"), TileScale.Eight)
        Tileimage1 = blockSettings.readImage("Tilemap")
        InterpretTiles()
    }
}
